package com.ecommercce.FirstSpring;

public class Marks {
	
	private int phy;
	private int chem;
	private int math;
	
	
	public int getPhy() {
		return phy;
	}
	public void setPhy(int phy) {
		this.phy = phy;
	}
	public int getChe() {
		return chem;
	}
	public void setChe(int che) {
		this.chem = che;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}

}
